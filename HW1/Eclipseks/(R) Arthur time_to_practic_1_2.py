dig_a = int(input("Введите число а: "))
dig_b = int(input("Введите число b: "))

print(f"{dig_a} + {dig_b} =", dig_a + dig_b)
print(f"{dig_a} - {dig_b} =", dig_a - dig_b)
print(f"{dig_a} * {dig_b} =", dig_a * dig_b)
print(f"{dig_a} / {dig_b} =", dig_a / dig_b)
print(f"{dig_a} ** {dig_b} =", dig_a ** dig_b)